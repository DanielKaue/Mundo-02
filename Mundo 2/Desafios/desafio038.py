n1 = int(input('Escreva um número inteiro: '))
n2 = int(input('Escreva outro: '))

if n1>n2:
    print('O número {} é maior que o número {}!'.format(n1, n2))

elif n1<n2:
    print('O número {} é menor que o número {}!'.format(n1, n2))

elif n1==n2:
    print('Os  {} e {} são iguais!'.format(n1, n2))
